import pytest

from src.processing import counting_categorys, filter_by_state, filter_transactions_by_description, sort_by_date


def test_1_filter_by_state(data_1):
    assert filter_by_state(data_1, "EXECUTED") == [
        {"id": 41428829, "state": "EXECUTED", "date": "2019-07-03T18:35:29.512364"},
        {"id": 939719570, "state": "EXECUTED", "date": "2018-06-30T02:08:58.425572"},
    ]
    assert filter_by_state(data_1, "CANCELED") == [
        {"id": 594226727, "state": "CANCELED", "date": "2018-09-12T21:27:25.241689"},
        {"id": 615064591, "state": "CANCELED", "date": "2018-10-14T08:21:33.419441"},
    ]
    assert filter_by_state(data_1, "SSSSSS") == []
    assert filter_by_state(data_1, "") == []


