def test_sort_by_date(data_1, expected_ascending_true, expected_ascending_false):
    assert sort_by_date(data_1) == expected_ascending_true
    assert sort_by_date(data_1, False) == expected_ascending_false


def test_filter_transactions_by_description(transactions):
    assert filter_transactions_by_description(transactions, "организации") == [
        {
            "id": 939719570,
            "state": "EXECUTED",
            "date": "2018-06-30T02:08:58.425572",
            "operationAmount": {"amount": "9824.07", "currency": {"name": "USD", "code": "USD"}},
            "description": "Перевод организации",
            "from": "Счет 75106830613657916952",
            "to": "Счет 11776614605963066702",
        },
        {
            "id": 594226727,
            "state": "CANCELED",
            "date": "2018-09-12T21:27:25.241689",
            "operationAmount": {"amount": "67314.70", "currency": {"name": "руб.", "code": "RUB"}},
            "description": "Перевод организации",
            "from": "Visa Platinum 1246377376343588",
            "to": "Счет 14211924144426031657",
        },
    ]
    assert filter_transactions_by_description(transactions, "adsad") == []


def test_counting_categorys(transactions):
    assert counting_categorys(transactions, ["Перевод организации", "Перевод с карты на карту"]) == {
        "Перевод организации": 2,
        "Перевод с карты на карту": 1,
    }
    assert counting_categorys(transactions, []) == {}
